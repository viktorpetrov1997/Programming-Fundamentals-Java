package ObjectsAndClasses.MoreExercises.RawData;

public class Engine
{
    int speed;
    int power;
    public Engine(int speed, int power)
    {
        this.speed = speed;
        this.power = power;
    }
    public int getSpeed()
    {
        return speed;
    }

    public int getPower()
    {
        return power;
    }
}
